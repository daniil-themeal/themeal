import fs from 'fs';
import path from 'path';
import { fileURLToPath } from 'url';

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const projectRoot = path.resolve(__dirname, '..');
const sourceDir = 'C:/Users/Bender/Desktop/svgIconsDesignSystem';
const targetDir = path.join(projectRoot, 'src/app/components/common/icons/svg');
const manifestPath = path.join(
  projectRoot,
  'src/app/components/common/icons/icon-manifest.json',
);
const nativeSizesPath = path.join(
  projectRoot,
  'src/app/components/common/icons/iconNativeSizes.ts',
);

const SIZES = [16, 20, 24, 32, 40, 48];
const STANDARD_NATIVE_SIZES = [16, 20, 24, 32, 40, 48];

const names = JSON.parse(fs.readFileSync(manifestPath, 'utf8'));

function normalizeSvg(content) {
  return content.replace(/stroke="#383E48"/g, 'stroke="currentColor"');
}

function slugify(name) {
  return name.trim().toLowerCase().replace(/\s+/g, '-');
}

function archiveFileName(index, size) {
  return index === 0 ? `Size=${size}.svg` : `Size=${size}-${index}.svg`;
}

function getNativeSizesForSlug(slug) {
  return STANDARD_NATIVE_SIZES.filter((size) =>
    fs.existsSync(path.join(targetDir, `${slug}-${size}.svg`)),
  );
}

function writeNativeSizesModule(nativeSizesBySlug) {
  const sortedEntries = [...nativeSizesBySlug.entries()].sort(([slugA], [slugB]) =>
    slugA.localeCompare(slugB),
  );

  const lookupEntries = sortedEntries
    .map(([slug, sizes]) => `  '${slug}': [${sizes.join(', ')}],`)
    .join('\n');

  const content = `/** Auto-generated by npm run icons:import — do not edit manually. */
import type { IconSize } from './iconSize';

export const FEATHER_NATIVE_SIZES_BY_SLUG = {
${lookupEntries}
} as const satisfies Record<string, readonly IconSize[]>;

export function getNativeIconSizes(slug: string): readonly IconSize[] {
  return FEATHER_NATIVE_SIZES_BY_SLUG[slug as keyof typeof FEATHER_NATIVE_SIZES_BY_SLUG] ?? [];
}

export function hasNativeIconSize(slug: string, size: IconSize): boolean {
  return getNativeIconSizes(slug).includes(size);
}
`;

  fs.writeFileSync(nativeSizesPath, content, 'utf8');
}

function updateManifestNativeSizes() {
  const updatedManifest = names.map((entry) => {
    const slug = entry.slug ?? slugify(entry.figmaName);

    return {
      ...entry,
      slug,
      nativeSizes: getNativeSizesForSlug(slug),
    };
  });

  fs.writeFileSync(manifestPath, `${JSON.stringify(updatedManifest, null, 2)}\n`, 'utf8');
  return updatedManifest;
}

fs.mkdirSync(targetDir, { recursive: true });

let copied = 0;
let missing = 0;

for (const entry of names) {
  const { index, figmaName } = entry;
  const slug = slugify(figmaName);

  for (const size of SIZES) {
    const sourceName = archiveFileName(index, size);
    const sourcePath = path.join(sourceDir, sourceName);
    const targetPath = path.join(targetDir, `${slug}-${size}.svg`);

    if (!fs.existsSync(sourcePath)) {
      missing += 1;
      console.warn(`missing: ${sourceName}`);
      continue;
    }

    const normalized = normalizeSvg(fs.readFileSync(sourcePath, 'utf8'));
    fs.writeFileSync(targetPath, normalized, 'utf8');
    copied += 1;
  }
}

const updatedManifest = updateManifestNativeSizes();
const nativeSizesBySlug = new Map(
  updatedManifest.map((entry) => [entry.slug, entry.nativeSizes]),
);
writeNativeSizesModule(nativeSizesBySlug);

console.log(`copied ${copied} files to ${targetDir}`);
if (missing) {
  console.log(`missing ${missing} source files`);
}
