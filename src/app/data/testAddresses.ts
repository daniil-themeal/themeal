export function searchTestUaeAddresses(query: string): TestAddress[] {
  const normalizedQuery = query.trim().toLowerCase();

  if (!normalizedQuery) return [];

  return testUaeAddresses
    .filter((address) => {
      const searchableText = [
        address.title,
        address.subtitle,
        address.city,
        address.emirate,
        address.countryCode,
        address.deliveryAvailability,
      ]
        .join(' ')
        .toLowerCase();

      return searchableText.includes(normalizedQuery);
    })
    .sort((a, b) => a.title.localeCompare(b.title));
}